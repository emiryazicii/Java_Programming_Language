package lesson06_IfStatements.practices;

public class MonthName {

    public static void main(String[] args) {

        int number = 5;

        String temp = "";

        if (number == 1) {
            temp = "January";
        }

        if (number == 2) {
            temp = "February";
        }

        if (number == 3) {
            temp = "March";
        }

        if (number == 4) {
            temp = "April";
        }

        if (number == 5) {
            temp = "May";
        }

        if (number == 6) {
            temp = "June";
        }

        if (number == 7) {
            temp = "July";
        }

        if (number == 8) {
            temp = "August";
        }

        if (number == 9) {
            temp = "September";
        }

        if (number == 10) {
            temp = "October";
        }

        if (number == 11) {
            temp = "November";
        }

        if (number == 12) {
            temp = "December";
        }

        System.out.println(temp);
    }
}

