package lesson02_EscapeSequences.practices;

public class Triangle {

    public static void main(String[] args) {

        System.out.println("\t\t\t     ^");
        System.out.println("\t\t\t    / \\");
        System.out.println("\t\t\t   /   \\");
        System.out.println("\t\t\t  /     \\");
        System.out.println("\t\t\t /       \\");
        System.out.println("\t\t\t ---------");

        System.out.println("\t\t\t     ^\n\t\t\t    / \\\n\t\t\t   /   \\\n\t\t\t  /     \\\n\t\t\t"+
                " /       \\\n\t\t\t ---------");
    }
}

