package lesson06_ifStatements;

public class SingleIfStatementsPractice {

    public static void main(String[] args) {

        int number = 1;

        if (number%2==0){

            System.out.println(number + " is even");
        }

        if (number%2!=0){

            System.out.println(number + " is odd");
        }
    }
}
