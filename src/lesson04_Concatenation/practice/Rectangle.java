public class Rectangle {

    public static void main(String[] args) {

        int width = 8,
                length = 125,
                area = width * length;

        System.out.println("area = " + area);
        System.out.println("---------------------");

        double width2 = 3.5,
                length2 = 7.8,
                area2 = width2 * length2;

        System.out.println("area2 = " + area2);
    }
}
