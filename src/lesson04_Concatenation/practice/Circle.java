package lesson04_Concatenation.practice;

public class Circle {

    public static void main(String[] args) {

        double radius = 125,
                area = 3.14 * radius * radius,
                perimeter = 2 * 3.14 * radius;

        System.out.println("\t\tArea of the circle is " + area + "\n\t\tPerimeter of the circle is " + perimeter);
    }
}

